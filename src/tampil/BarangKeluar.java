            String sql = "SELECT * FROM barang_keluar WHERE kode_barang_keluar like '%"+cariitem+"%' order by kode_barang_keluar asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
